export const getRandomArray = (arr: Array<any>) => {
  return arr[Math.floor(Math.random() * arr.length)];
};

export const shuffleArray = (array: Array<any>) => {
  for (let i = array.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    const temp = array[i];
    array[i] = array[j];
    array[j] = temp;
  }
};
